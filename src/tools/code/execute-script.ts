export const writeScriptTool = new DynamicStructuredTool({
  name: 'write_script',
  description: `Write a Bun TypeScript script to ~/.dexter/scripts/.
Use this to create data collection scripts that will be run on a schedule.
Scripts can:
  - Fetch data from financial APIs (FINANCIAL_DATASETS_API_KEY is available)
  - Write output to DEXTER_COLLECTED_DIR (available as env var)
  - Use Bun built-ins: fetch, Bun.write, Bun.file, etc.
  - Import from "node:fs/promises", "node:path", "node:os"
Scripts CANNOT: access LLM APIs, access ~/.dexter/credentials, or run subprocesses.
Always use process.env.DEXTER_COLLECTED_DIR as the output root.`,

  schema: z.object({
    filename: z.string().describe('Script filename (e.g. "AAPL-earnings-2026-Q1-collect.ts")'),
    code: z.string().describe('TypeScript code for the script'),
    description: z.string().describe('What this script does'),
  }),

  func: async ({ filename, code, description }) => {
    const dir = getScriptsDir();
    await mkdir(dir, { recursive: true });

    // Sanitize filename
    const safeName = filename.replace(/[^a-zA-Z0-9._-]/g, '-');
    const scriptPath = join(dir, safeName);

    const header = `// ${description}
// Generated by Dexter autonomous agent on ${new Date().toISOString()}
// Run: bun run ${scriptPath}
`;

    await writeFile(scriptPath, header + code, 'utf-8');

    return formatToolResult({
      scriptPath,
      filename: safeName,
      bytes: code.length,
      message: `Script written to ${scriptPath}`,
    });
  },
});

