export const testScriptTool = new DynamicStructuredTool({
  name: 'test_script',
  description: `Test a previously written script by running it in a sandboxed subprocess.
Verifies the script executes without errors and produces expected output.
Always test a script before scheduling it.
Returns: stdout, stderr, exit code, and success/failure assessment.`,

  schema: z.object({
    filename: z.string().describe('Script filename (just the filename, not full path)'),
    expectedOutputPattern: z
      .string()
      .optional()
      .describe('Optional string/pattern that must appear in stdout to confirm success'),
    timeoutMs: z.number().default(30000).describe('Max execution time in milliseconds'),
  }),

  func: async ({ filename, expectedOutputPattern, timeoutMs }) => {
    const safeName = filename.replace(/[^a-zA-Z0-9._-]/g, '-');
    const scriptPath = join(getScriptsDir(), safeName);

    // Check script exists
    try {
      await stat(scriptPath);
    } catch {
      return formatToolResult({
        success: false,
        error: `Script not found: ${scriptPath}. Use write_script first.`,
      });
    }

    const result = await runScript(scriptPath, timeoutMs);

    const patternMatch =
      !expectedOutputPattern ||
      result.stdout.includes(expectedOutputPattern);

    const success =
      !result.timedOut &&
      result.exitCode === 0 &&
      patternMatch;

    return formatToolResult({
      success,
      exitCode: result.exitCode,
      timedOut: result.timedOut,
      patternMatched: patternMatch,
      stdout: result.stdout.slice(0, 3000),
      stderr: result.stderr.slice(0, 1000),
      scriptPath,
      message: success
        ? 'Script test passed — safe to schedule.'
        : `Script test FAILED: ${result.timedOut ? 'timeout' : `exit ${result.exitCode}`}. Fix before scheduling.`,
    });
  },
});

