/**
 * Script execution tools — the agent's code execution environment.
 *
 * write_script: Write a TypeScript script to ~/.dexter/scripts/
 * test_script:  Run a script in a sandboxed subprocess and verify it works
 * run_script:   Execute a script in production (after testing)
 *
 * Security model:
 * - Scripts run in a child process with a stripped environment
 * - No access to API keys or credentials (unless explicitly injected via safe allowlist)
 * - Configurable timeout (default 30s) and stdout size limit
 * - Files restricted to ~/.dexter/ output directory
 */

import { DynamicStructuredTool } from '@langchain/core/tools';
import { mkdir, writeFile, readFile, stat } from 'node:fs/promises';
import { join, resolve, isAbsolute } from 'node:path';
import { homedir } from 'node:os';
import { z } from 'zod';
import { formatToolResult } from '../types.js';
import { getScriptsDir, getCollectedDataDir } from '../../daemon/pipelines.js';

const MAX_OUTPUT_BYTES = 50_000; // 50KB max stdout
const DEFAULT_TIMEOUT_MS = 30_000;

/**
 * Environment variable allowlist for sandboxed scripts.
 *
 * EXPLICITLY EXCLUDED (never passed to scripts):
 *   OPENAI_API_KEY, ANTHROPIC_API_KEY, GOOGLE_API_KEY — LLM credentials
 *   TELEGRAM_BOT_TOKEN — messaging credentials
 *   LANGCHAIN_API_KEY, LANGSMITH_API_KEY — observability credentials
 *   Any other secret not in this list
 *
 * Included: read-only data API keys and Dexter path configuration only.
 */
const SAFE_ENV_KEYS = [
  'HOME',
  'PATH',
  'FINANCIAL_DATASETS_API_KEY', // read-only financial data API
  'EXASEARCH_API_KEY',           // read-only web search
  'TAVILY_API_KEY',              // read-only web search fallback
  'DEXTER_SCRIPTS_DIR',
  'DEXTER_COLLECTED_DIR',
  'DEXTER_OUTPUT_DIR',
  'DEXTER_OUTPUT_PATH',          // canonical per-run output path set by pipeline runner
];

function buildSafeEnv(): Record<string, string> {
  const env: Record<string, string> = {
    DEXTER_SCRIPTS_DIR: getScriptsDir(),
    DEXTER_COLLECTED_DIR: getCollectedDataDir(),
    DEXTER_OUTPUT_DIR: join(homedir(), '.dexter', 'output'),
  };
  for (const key of SAFE_ENV_KEYS) {
    const val = process.env[key];
    if (val) env[key] = val;
  }
  return env;
}

async function runScript(
  scriptPath: string,
  timeoutMs: number = DEFAULT_TIMEOUT_MS
): Promise<{ stdout: string; stderr: string; exitCode: number; timedOut: boolean }> {
  // Ensure output directory exists
  await mkdir(join(homedir(), '.dexter', 'output'), { recursive: true });

  const proc = Bun.spawn(['bun', 'run', '--smol', scriptPath], {
    stdout: 'pipe',
    stderr: 'pipe',
    env: buildSafeEnv(),
    cwd: join(homedir(), '.dexter'),
  });

  let timedOut = false;
  const timeoutHandle = setTimeout(() => {
    timedOut = true;
    try { proc.kill(); } catch { /* already dead */ }
  }, timeoutMs);

  const [exitCode, stdoutBuf, stderrBuf] = await Promise.all([
    proc.exited,
    new Response(proc.stdout).arrayBuffer(),
    new Response(proc.stderr).arrayBuffer(),
  ]);

  clearTimeout(timeoutHandle);

  const decoder = new TextDecoder();
  let stdout = decoder.decode(stdoutBuf);
  let stderr = decoder.decode(stderrBuf);

  // Truncate oversized output
  if (stdout.length > MAX_OUTPUT_BYTES) {
    stdout = stdout.slice(0, MAX_OUTPUT_BYTES) + '\n[output truncated]';
  }
  if (stderr.length > MAX_OUTPUT_BYTES) {
    stderr = stderr.slice(0, MAX_OUTPUT_BYTES) + '\n[stderr truncated]';
  }

  return { stdout, stderr, exitCode, timedOut };
}

