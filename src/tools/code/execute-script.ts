export const runScriptTool = new DynamicStructuredTool({
  name: 'run_script',
  description: `Execute a collection script that has already been tested.
Used by pipeline runner to collect data on schedule.
Returns the script's stdout output and any files written to DEXTER_COLLECTED_DIR.`,

  schema: z.object({
    scriptPath: z.string().describe('Full path to the script to run'),
    timeoutMs: z.number().default(60000).describe('Max execution time in ms (default 60s)'),
  }),

  func: async ({ scriptPath, timeoutMs }) => {
    const result = await runScript(scriptPath, timeoutMs);

    return formatToolResult({
      exitCode: result.exitCode,
      timedOut: result.timedOut,
      success: !result.timedOut && result.exitCode === 0,
      stdout: result.stdout,
      stderr: result.stderr.slice(0, 500),
    });
  },
});

